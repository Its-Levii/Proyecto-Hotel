    public void llenarTodas(){
        txtDocumento.setVisible(false);
        btnBuscar.setVisible(false);
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(encabezado);
        
        for (String[] fila : checkin.EstadiaActiva("Todo", 0)) {
            modelo.addRow(fila);
        }
        
        tableHistorialEstadias.setModel(modelo);
    }    
    public void llenarEspecifico(){
        String obtener_documento = txtDocumento.getText();
        int documento = 0;
        boolean completo = true;
        if (obtener_documento.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "El documento no puede estar vacio");
        }else{
            try {
                documento = Integer.parseInt(obtener_documento);
                completo = true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "El documento solo debe contener numeros");
            }
        }
        
        if (completo){
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(encabezado);
        
            for (String[] fila : checkin.EstadiaActiva("Documento", documento)) {
                modelo.addRow(fila);
            }
        
            tableHistorialEstadias.setModel(modelo);
        }
        
    }

