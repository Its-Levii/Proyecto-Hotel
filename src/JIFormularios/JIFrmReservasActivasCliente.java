    public void llenarActivas(){
        int enviar;
        if (usuario.datosUsuario(id)[9].equals("admin")){
            enviar = 0;
        }else{
            enviar = id;
        }
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(encabezado);
        for (String[] fila : reserva.ReservasDeUsuario(enviar)) {
            if (fila[7].equals("Pendiente")){
                modelo.addRow(fila);
            }
        }
        tableReservasActivas.setModel(modelo);
    }
    public void llenarTodas(){
        int enviar;
        if (usuario.datosUsuario(id)[9].equals("admin")){
            enviar = 0;
        }else{
            enviar = id;
        }
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(encabezado);
        for (String[] fila : reserva.ReservasDeUsuario(enviar)) {
            modelo.addRow(fila);
        }
        tableReservasActivas.setModel(modelo);
    }

